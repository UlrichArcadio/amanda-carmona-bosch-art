var seriesCarousel1 = document.querySelector('#carouselSeriesControl1')
var seriesModalEl1 = document.getElementById('seriesModal1')
seriesModalEl1.addEventListener('show.bs.modal', function (event) {
    const trigger = event.relatedTarget
    var bsCarousel = bootstrap.Carousel.getInstance(seriesCarousel1)
    bsCarousel.to(trigger.dataset.bsSlideTo)
})


